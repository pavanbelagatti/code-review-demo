def add_numbers(a, b):
    """
    This function adds two numbers.
    """
    sum = a + b
    return sum

def multiply_numbers(x, y):
    product = x * y
    return product

# Example usage
result_sum = add_numbers(5, 3)
print(f"The sum is: {result_sum}")
result_product = multiply_numbers(4,6)
print(f"The product is: {result_product}")